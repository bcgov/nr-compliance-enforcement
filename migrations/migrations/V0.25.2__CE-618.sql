CREATE
or REPLACE TRIGGER actntkn_history_trigger BEFORE INSERT
OR DELETE
OR
UPDATE ON action_taken FOR EACH ROW
EXECUTE PROCEDURE audit_history ('action_taken_h', 'action_taken_guid');

